package com.kodilla.csvconverter;

public class PersonWithBirthYear {
    private String name;
    private String surname;
    private int birthYeah;

    public PersonWithBirthYear() {
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getBirthYeah() {
        return birthYeah;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public void setBirthYeah(int birthYeah) {
        this.birthYeah = birthYeah;
    }
}
